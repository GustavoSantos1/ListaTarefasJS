function funcaox (){
    var y = "Alo";
    return y;
}
// setInterval - Repete a execução de uma função(passada por parâmetro) por um determinado periodo de tempo(passado por parâmetro).
setInterval(funcaox, 1000);
// executa funcaox a cada 1 segundo.

// setTimeout - Executa uma função(passada por parâmentro) apenas uma vez, porém com um tempo de delay(passado por parâmetro) até executa-la.
setTimeout(funcaox, 5000);
// executa a funcaox 5 segundos após ela ter sido chamada.



